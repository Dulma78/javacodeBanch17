package class2;

public class E7ArithmeticOperators {
    public static void main(String[] args) {
        double num1=10;
        double num2=3;
        System.out.println(num1/num2);
        System.out.println(num1%num2);
        int noOfSlices=5;
        int noOfFriends=2;
        System.out.println(noOfSlices/noOfFriends);
        System.out.println(noOfSlices%noOfFriends);
        //10/3 3.33333

    }
}
